const A = 3.0
const B = 3.5
const C = 4.5

const AreaTrianguloRetangulo = (A * C) / 2

const AreaDoCirculo =  3.14159 * (C**2)

const AreaDoTrapezio = ((A + B) * C) / 2

const AreaDoQuadrado = B**2

const AreaDoRetangulo = A * B

console.log("TRIANGULO: ", AreaTrianguloRetangulo);
console.log("CIRCULO: ", AreaDoCirculo);
console.log("TRAPEZIO: ", AreaDoTrapezio);
console.log("QUADRADO: ", AreaDoQuadrado);
console.log("RETANGULO: ", AreaDoRetangulo);
