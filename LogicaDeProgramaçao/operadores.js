
function somar(num1, num2){// funçao que soma dois numeros
    const resultado = num1 + num2 // variavel que guarda a soma dos 2 numeros passados na funçao
    console.log(`o resultado da soma dos numeros ${num1} + ${num2} é `,resultado);// imprimi o resultado
}

somar(5, 5)// chama a funçao somar e passar os valores/argumentos dentro dos parenteses


function subtrair(num1, num2){
    const resultado = num1 - num2
    console.log(`o resultado do subtraçao de ${num1} - ${num2} é `,resultado);
}

subtrair(10, 5)

function multiplicar(num1, num2){
    const resultado = num1 * num2
    console.log(`o resultado da multiplicaçao de ${num1} * ${num2} é `, resultado);
}

multiplicar(5, 5)

